/*********************************************************************
The following error display functions are specific to edbrowse,
rather than extended versions of the standard unix print functions.
Thus I don't need the i_ prefix.
*********************************************************************/

char errorMsg[4000];

/* Show the error message, not just the question mark, after these commands. */
static const char showerror_cmd[] = "AbefMqrw^";

/* Set the error message.  Type h to see the message. */
void setError(int msg, ...)
{
	va_list p;

	if (msg < 0) {
		errorMsg[0] = 0;
		return;
	}

	va_start(p, msg);
	vsprintf(errorMsg, getString(msg), p);
	va_end(p);

/* sanity check */
	if (strlen(errorMsg) >= sizeof(errorMsg)) {
		i_printf(MSG_ErrorMessageLong, strlen(errorMsg));
		puts(errorMsg);
		exit(1);
	}
}				/* setError */

void showError(void)
{
	if (errorMsg[0])
		puts(errorMsg);
	else
		i_puts(MSG_NoErrors);
}				/* showError */

void showErrorConditional(char cmd)
{
	if (helpMessagesOn || strchr(showerror_cmd, cmd))
		showError();
	else
		puts("?");
}				/* showErrorConditional */

void showErrorAbort(void)
{
	errorPrint("1%s", errorMsg);
}				/* showErrorAbort */

void browseError(int msg, ...)
{
	va_list p;
	if (ismc)
		return;
	if (browseLocal != 1)
		return;
	if (browseLine) {
		i_printf(MSG_LineX, browseLine);
		cw->labels[4] = browseLine;
	} else
		i_printf(MSG_BrowseError);
	va_start(p, msg);
	vprintf(getString(msg), p);
	va_end(p);
	nl();
	browseLocal = 2;
}				/* browseError */

/* Javascript errors, we need to see these no matter what. */
void runningError(int msg, ...)
{
	va_list p;
	if (ismc)
		return;
	if (debugLevel <= 2)
		return;
	if (browseLine) {
		i_printf(MSG_LineX, browseLine);
		cw->labels[4] = browseLine;
	}
	va_start(p, msg);
	vprintf(getString(msg), p);
	va_end(p);
	nl();
	browseLocal = 2;
}				/* runningError */

