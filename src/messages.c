/* English by default */
static const char **messageArray = englishMessages;
static int messageArrayLength = sizeof(englishMessages) / sizeof(char *);

int eb_lang = 1;		/* default English */
bool cons_utf8, iuConvert = true;
char type8859 = 1;

void selectLanguage(void)
{
	char buf[8];
	char *s = getenv("LANG");
	if (!s)
		return;
	if (!*s)
		return;

	if (strstrCI(s, "utf8") || strstrCI(s, "utf-8"))
		cons_utf8 = true;

/* We roll our own international messages in this file, so you wouldn't think
 * we need setlocale, but pcre needs the locale for expressions like \w,
 * and for ranges like [A-z],
 * and to convert to upper or lower case etc.
 * So I set LC_ALL, which covers both LC_CTYPE and LC_COLLATE.
 * Note however that your environment does not affect the order of files
 * in a directory listing.
 * I do the sort myself, using strcmp, rather than strcoll.
 * See qscmp() in stringfile.c */

	setlocale(LC_ALL, "");

	strncpy(buf, s, 7);
	buf[7] = 0;
	caseShift(buf, 'l');

	if (!strncmp(buf, "en", 2))
		return;		/* english is default */

	if (!strncmp(buf, "fr", 2)) {
		eb_lang = 2;
		messageArray = frenchMessages;
		messageArrayLength = sizeof(frenchMessages) / sizeof(char *);
		return;
	}

	if (!strncmp(buf, "pt_br", 5)) {
		eb_lang = 3;
		messageArray = brazilianPortugueseMessages;
		messageArrayLength =
		    sizeof(brazilianPortugueseMessages) / sizeof(char *);
		return;
	}

	if (!strncmp(buf, "pl", 2)) {
		eb_lang = 4;
		messageArray = polishMessages;
		messageArrayLength = sizeof(polishMessages) / sizeof(char *);
		type8859 = 2;
		return;
	}

	if (!strncmp(buf, "de", 2)) {
		eb_lang = 5;
		messageArray = germanMessages;
		messageArrayLength = sizeof(germanMessages) / sizeof(char *);
		type8859 = 1;
		return;
	}

/* This message is really annoying if it pops up every time you invoke edbrowse.
	fprintf(stderr, "Sorry, language %s is not implemented\n", buf);
*/
}				/* selectLanguage */

static const char *getString(int msg)
{
	const char **a = messageArray;
	const char *s;
	char *t;
	int t_len;
	static char utfbuf[1000];

	if (msg >= messageArrayLength)
		a = englishMessages;
	s = a[msg];
	if (!s)
		s = englishMessages[msg];
	if (!s)
		s = "spurious message";

	if (cons_utf8)
		return s;

/* We have to convert it. */
	utf2iso(s, strlen(s), &t, &t_len);
	strcpy(utfbuf, t);
	nzFree(t);
	return utfbuf;
}				/* getString */

